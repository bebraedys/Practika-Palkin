-- 1. partner_types - Справочник типов партнеров
-- ============================================================================
-- | Column  | Type         | Constraints        | Description           |
-- |---------|--------------|--------------------|-----------------------|
-- | id      | SERIAL       | PRIMARY KEY        | Идентификатор типа    |
-- | name    | VARCHAR(100) | NOT NULL, UNIQUE   | Название типа         |
-- ============================================================================

-- 2. partners - Партнеры компании
-- ============================================================================
-- | Column        | Type          | Constraints              | Description       |
-- |---------------|---------------|--------------------------|-------------------|
-- | id            | SERIAL        | PRIMARY KEY              | Идентификатор     |
-- | partner_type_id| INTEGER      | FK → partner_types.id    | Тип партнера      |
-- | name          | VARCHAR(255)  | NOT NULL                 | Наименование      |
-- | legal_address | VARCHAR(500)  |                          | Юр. адрес         |
-- | inn           | VARCHAR(20)   |                          | ИНН               |
-- | director_name | VARCHAR(255)  |                          | ФИО директора     |
-- | phone         | VARCHAR(50)   |                          | Телефон           |
-- | email         | VARCHAR(100)  |                          | Email             |
-- | rating        | INTEGER       | CHECK (rating >= 0)      | Рейтинг           |
-- | created_at    | TIMESTAMP     | DEFAULT CURRENT_TIMESTAMP| Дата создания     |
-- | updated_at    | TIMESTAMP     | DEFAULT CURRENT_TIMESTAMP| Дата обновления   |
-- ============================================================================

-- 3. sales - История продаж продукции
-- ============================================================================
-- | Column      | Type          | Constraints              | Description       |
-- |-------------|---------------|--------------------------|-------------------|
-- | id          | SERIAL        | PRIMARY KEY              | Идентификатор     |
-- | partner_id  | INTEGER       | FK → partners.id         | Партнер           |
-- | product_name| VARCHAR(255)  | NOT NULL                 | Наименование      |
-- | quantity    | INTEGER       | CHECK (quantity > 0)     | Количество        |
-- | sale_date   | DATE          | DEFAULT CURRENT_DATE     | Дата продажи      |
-- | amount      | NUMERIC(12,2) | CHECK (amount >= 0)      | Сумма (руб)       |
-- ============================================================================

-- ============================================================================
-- СВЯЗИ
-- ============================================================================

-- partner_types (1) ──────< partners (N)
-- Тип партнера имеет множество партнеров

-- partners (1) ──────< sales (N)  
-- Партнер имеет множество продаж

